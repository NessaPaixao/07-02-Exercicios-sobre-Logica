valor1 = float(input("Digite um numero: "))
valor2 = float(input("Digite um segundo numero: "))

def soma (valor1, valor2):
    return valor1+valor2

def sub (valor1, valor2):
    return valor1-valor2

def mult (valor1, valor2):
    return valor1*valor2

def div (valor1, valor2):
    return valor1/valor2

print(soma(valor1,valor2))
print(sub(valor1,valor2))
print(mult(valor1,valor2))
print(div(valor1,valor2))